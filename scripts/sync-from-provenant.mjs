#!/usr/bin/env node
// Sync generated artifacts from the flagship Provenant repo into this site so
// the homepage/comparison stay in step with what the repo produces.
//
//   node scripts/sync-from-provenant.mjs [path-to-provenant-repo]
//
// Default source is a sibling checkout: ../provenant. Regenerate the flagship
// artifacts first (docs/BENCHMARKS.md, docs/scan-duration-vs-files.svg), then
// run this. It updates:
//   - src/data/benchmarks.json   (headline speed figures parsed from BENCHMARKS.md)
//   - public/assets/scan-duration-vs-files.svg
//   - public/assets/provenant-demo.gif
//
// The per-repo comparison specifics on /compare/ come from ad-hoc `compare-outputs`
// runs, not a standing artifact, so they remain hand-maintained.
import { readFileSync, writeFileSync, copyFileSync, existsSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join, resolve } from 'node:path';

const here = dirname(fileURLToPath(import.meta.url));
const siteRoot = resolve(here, '..');
const src = resolve(siteRoot, process.argv[2] ?? '../provenant');

if (!existsSync(src)) {
  console.error(`Provenant repo not found at ${src}. Pass its path as an argument.`);
  process.exit(1);
}

const md = readFileSync(join(src, 'docs/BENCHMARKS.md'), 'utf8');

// Package version from the flagship Cargo.toml, so the version string on
// /compare/ never drifts from what the repo actually ships.
const cargo = readFileSync(join(src, 'Cargo.toml'), 'utf8');
const version = (cargo.match(/^version\s*=\s*"([^"]+)"/m) ?? [])[1];
if (!version) throw new Error('Cargo.toml: could not parse package version');

// Headline blockquote, e.g. "faster on 254 of 254 recorded runs, with a
// **19.7× median speedup** and **19.7× geometric-mean speedup** overall; the
// median gap grows from **9.1×** ... to **37.2×** ..."
const num = (re) => {
  const m = md.match(re);
  if (!m) throw new Error(`BENCHMARKS.md: could not parse ${re}`);
  return m[1];
};
const round = (s) => String(Math.round(parseFloat(s) * 10) / 10);

const runs = num(/on (\d+) of \d+ recorded runs/);
const median = round(num(/\*\*([\d.]+)×\s*median speedup\*\*/));
const geomean = round(num(/\*\*([\d.]+)×\s*geometric-mean speedup\*\*/));
const low = String(Math.round(parseFloat(num(/grows from \*\*([\d.]+)×\*\*/))));
const high = String(Math.round(parseFloat(num(/to \*\*([\d.]+)×\*\*/))));

// Fastest single row: scan all "— **NN.N× faster**" headings, keep the max.
// Also capture the astral-sh/uv row, which backs the homepage demo card
// (the demo GIF races the uv repository).
let fastest = 0;
let fastestRepo = '';
let demoSpeedup = '';
for (const m of md.matchAll(/\[([^\]]+?)(?:\s*@[^\]]+)?\]\([^)]*\)\s*—\s*\*\*([\d.]+)×\s*faster\*\*/g)) {
  const v = parseFloat(m[2]);
  if (v > fastest) {
    fastest = v;
    fastestRepo = m[1].split('/').pop();
  }
  if (m[1] === 'astral-sh/uv') demoSpeedup = round(m[2]);
}

const data = {
  _comment: 'Generated by scripts/sync-from-provenant.mjs from ../provenant/docs/BENCHMARKS.md and Cargo.toml. Do not edit by hand; run `npm run sync`.',
  version,
  runs: Number(runs),
  medianSpeedup: median,
  geomeanSpeedup: geomean,
  sizeRangeLow: low,
  sizeRangeHigh: high,
  fastest: fastest ? round(String(fastest)) : '70.7',
  fastestRepo: fastestRepo || 'CPython',
  demoRepo: 'astral-sh/uv',
  demoSpeedup: demoSpeedup || '28.8',
};
writeFileSync(join(siteRoot, 'src/data/benchmarks.json'), JSON.stringify(data, null, 2) + '\n');

for (const asset of ['scan-duration-vs-files.svg', 'provenant-demo.gif']) {
  const from = join(src, 'docs', asset);
  if (existsSync(from)) copyFileSync(from, join(siteRoot, 'public/assets', asset));
}

console.log('Synced from', src, '->', data);
